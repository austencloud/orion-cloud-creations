					<h3 class="text-lg font-medium text-charcoal mb-3">Spiral</h3>
					<div class="space-y-3 text-muted text-sm leading-relaxed">
						<p>
							The spiral is the foundation of tie-dye. Pinch the fabric at the center point, then twist. The entire garment wraps into a flat disc. Rubber bands section it into wedges, and each wedge gets a different color.
						</p>
						<p>
							What separates a professional spiral from a beginner one is color theory, saturation, and technique. I use complementary and analogous color schemes with controlled dye application to prevent muddy blending.
						</p>
						<p>
							Reverse spirals use a black or dark base and bleach or discharge the color out before re-dyeing. This creates intense contrast with black channels running through the spiral arms.
						</p>
					</div>
				</div>
			</div>

			<!-- Mandala -->
			<div class="grid grid-cols-1 lg:grid-cols-2 gap-12 items-center">
				<div class="order-2 lg:order-1">
					<h3 class="text-lg font-medium text-charcoal mb-3">Mandala</h3>
					<div class="space-y-3 text-muted text-sm leading-relaxed">
						<p>
							Mandalas are created by folding the fabric into a symmetrical pattern (usually sixths or eighths) and then applying dye in concentric rings from the center outward. The fold creates the radial symmetry.
						</p>
						<p>
							Combined with ice dye, mandalas take on an ethereal quality. The ice melt creates organic texture within the geometric structure, blending the precision of the fold with the chaos of the melt.
						</p>
						<p>
							These are the most technically demanding pieces. The folding must be exact, the dye placement must be deliberate, and the color palette must be cohesive across multiple concentric zones.
						</p>
