	<!-- Care Instructions -->
	<section class="care-section">
		<h2 class="heading-lg">Care Instructions</h2>
		<div class="care-grid">
			<div>
				<h3 class="care-heading">First Wash</h3>
				<ul class="care-list">
					<li>Wash alone in cold water for the first wash</li>
					<li>A small amount of excess dye may rinse out. This is normal.</li>
					<li>Use a gentle detergent (no bleach)</li>
					<li>Tumble dry low or hang dry</li>
				</ul>
			</div>
			<div>
				<h3 class="care-heading">Ongoing Care</h3>
				<ul class="care-list">
					<li>Machine wash with like colors in cold or warm water</li>
					<li>Tumble dry on any setting</li>
					<li>No bleach or oxiclean</li>
					<li>Colors are permanent. They will not fade or bleed after the first wash.</li>
				</ul>
			</div>
