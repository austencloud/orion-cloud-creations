			<div>
				<h2 class="heading-lg">Procion MX Fiber Reactive Dyes</h2>
				<div class="body-text">
					<p>
						Every piece uses Procion MX dyes. These are fiber reactive dyes, meaning they form a permanent covalent bond with cellulose (cotton) fiber at the molecular level. The dye literally becomes part of the fabric.
					</p>
					<p>
						This is different from the all-purpose dyes you find at grocery stores. Those sit on top of the fiber and wash out over time. Procion MX goes through the fiber. You can machine wash and dry these pieces normally. The color is permanent.
					</p>
					<p>
						The chemical reaction requires soda ash (sodium carbonate) as a fixative and time. Each piece sits in dye for 24 to 48 hours while the reaction completes. There are no shortcuts.
					</p>
				</div>
