					<h3 class="text-lg font-medium text-charcoal mb-3">Ice Dye</h3>
					<div class="space-y-3 text-muted text-sm leading-relaxed">
						<p>
							The fabric is folded and placed on a rack over a container. Ice is piled on top, then dry dye powder is sprinkled directly onto the ice. As the ice melts over hours, it carries the dye through the fabric in unpredictable, organic patterns.
						</p>
						<p>
							The slow melt creates watercolor-like effects with incredible depth and detail. No two ice melts produce the same result. The crystalline patterns, color bleeding, and gradient effects are unique to each piece.
						</p>
						<p>
							Ice dye produces the most complex and visually rich results of any tie-dye technique. It's also the most unpredictable, which is part of what makes it interesting.
						</p>
					</div>
				</div>
			</div>

			<!-- Shibori -->
			<div class="grid grid-cols-1 lg:grid-cols-2 gap-12 items-center">
				<div class="order-2 lg:order-1">
					<h3 class="text-lg font-medium text-charcoal mb-3">Shibori</h3>
					<div class="space-y-3 text-muted text-sm leading-relaxed">
						<p>
							Shibori is a Japanese resist-dyeing technique with centuries of history. The fabric is folded into precise geometric patterns, then bound, clamped, or stitched to create resist areas that prevent dye from penetrating.
						</p>
						<p>
							The precision of the fold determines the symmetry of the final pattern. Accordion folds create ripple effects. Triangle folds create geometric kaleidoscopes. The technique demands patience and accuracy.
						</p>
						<p>
							When combined with multiple dye colors, shibori creates stunning geometric compositions that look architectural but are made entirely through fabric manipulation.
						</p>
					</div>
				</div>
