	<section>
		<h2 class="heading-lg techniques-heading">Techniques</h2>

		<div class="technique-list">
			<!-- Ice Dye -->
			<div class="two-col">
				<img
					src="/images/products/blue-mandala.jpg"
					alt="Ice dye mandala showing crystalline organic patterns"
					class="section-img"
					loading="lazy"
				/>
				<div>
					<h3 class="heading-md">Ice Dye</h3>
					<div class="body-text">
						<p>
							The fabric is folded and placed on a rack over a container. Ice is piled on top, then dry dye powder is sprinkled directly onto the ice. As the ice melts over hours, it carries the dye through the fabric in unpredictable, organic patterns.
						</p>
						<p>
							The slow melt creates watercolor-like effects with incredible depth and detail. No two ice melts produce the same result. The crystalline patterns, color bleeding, and gradient effects are unique to each piece.
						</p>
						<p>
							Ice dye produces the most complex and visually rich results of any tie-dye technique. It's also the most unpredictable, which is part of what makes it interesting.
						</p>
					</div>
				</div>
			</div>

			<!-- Shibori -->
			<div class="two-col reverse">
				<div>
					<h3 class="heading-md">Shibori</h3>
					<div class="body-text">
						<p>
							Shibori is a Japanese resist-dyeing technique with centuries of history. The fabric is folded into precise geometric patterns, then bound, clamped, or stitched to create resist areas that prevent dye from penetrating.
						</p>
						<p>
							The precision of the fold determines the symmetry of the final pattern. Accordion folds create ripple effects. Triangle folds create geometric kaleidoscopes. The technique demands patience and accuracy.
						</p>
						<p>
							When combined with multiple dye colors, shibori creates stunning geometric compositions that look architectural but are made entirely through fabric manipulation.
						</p>
					</div>
				</div>
				<img
					src="/images/products/golden-shibori-grid.jpg"
					alt="Shibori grid pattern in gold, orange, and purple"
					class="section-img"
					loading="lazy"
				/>
			</div>

			<!-- Spiral -->
			<div class="two-col">
				<img
					src="/images/products/classic-spiral-rainbow.jpg"
					alt="Classic rainbow spiral pattern on black"
					class="section-img"
					loading="lazy"
				/>
				<div>
					<h3 class="heading-md">Spiral</h3>
					<div class="body-text">
						<p>
							The spiral is the foundation of tie-dye. Pinch the fabric at the center point, then twist. The entire garment wraps into a flat disc. Rubber bands section it into wedges, and each wedge gets a different color.
						</p>
						<p>
							What separates a professional spiral from a beginner one is color theory, saturation, and technique. I use complementary and analogous color schemes with controlled dye application to prevent muddy blending.
						</p>
						<p>
							Reverse spirals use a black or dark base and bleach or discharge the color out before re-dyeing. This creates intense contrast with black channels running through the spiral arms.
						</p>
					</div>
				</div>
			</div>

			<!-- Mandala -->
			<div class="two-col reverse">
				<div>
					<h3 class="heading-md">Mandala</h3>
					<div class="body-text">
						<p>
							Mandalas are created by folding the fabric into a symmetrical pattern (usually sixths or eighths) and then applying dye in concentric rings from the center outward. The fold creates the radial symmetry.
						</p>
						<p>
							Combined with ice dye, mandalas take on an ethereal quality. The ice melt creates organic texture within the geometric structure, blending the precision of the fold with the chaos of the melt.
						</p>
						<p>
							These are the most technically demanding pieces. The folding must be exact, the dye placement must be deliberate, and the color palette must be cohesive across multiple concentric zones.
						</p>
					</div>
				</div>
				<img
